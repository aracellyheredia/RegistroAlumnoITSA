     public void mostrar(){
         
         try{
            String matris[][]=new String [lista.size()][4];
         
         for (int i=0; i<lista.size(); i++){
             matris[i][0]=lista.get(i).getNombre();
             matris[i][1]=lista.get(i).getApellidos();
             matris[i][2]=lista.get(i).getMatricula();
             matris[i][3]=String.valueOf(lista.get(i+1).getTelefono());
             
             jTableDatos.setModel(new javax.swing.table.DefaultTableModel(
            matris,
            new String [] {
                "Nombre", "Apellido", "Matricula", "Telefono"
            }
        ));
         }
        
        }
        catch(IndexOutOfBoundsException m){
            
            System.out.println("Intento de acceder a una columna 5");
        }
     }
